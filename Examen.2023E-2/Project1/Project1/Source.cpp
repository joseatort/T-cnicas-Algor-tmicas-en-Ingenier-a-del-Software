using matriz = vector<vector<int>>;

int ProgDinamica(int D, int T)
{
    vector<int> aux(T + 1, 0);
    aux[0] = 1;

    for (int i = 1; i <= D; i++)
        for (int j = i; j <= T; j++)
            aux[j] = aux[j] + aux[j - i];

    return aux[T];
}

void resuelveCaso() {

    int D, T;
    cin >> D >> T;
    cout << ProgDinamica(D, T) << "\n";
}

