int ProgramacionDinamica(int N, int objetivo) {

    vector<int> f(objetivo + 1, 0);
    f[0] = 1;
    for (int i = 1; i <= N; ++i)
        for (int j = i; j <= objetivo; ++j)
            f[j] = f[j] + f[j - i];
        
    return f[objetivo];
}

